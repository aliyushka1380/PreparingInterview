class ItemDiscount:
    def __init__(self, name, price):
        self.__name = name
        self.__price = price


class ItemDiscountReport(ItemDiscount):
    def get_parent_data(self):
        return f'Наименование товара: {self.name}. Цена: {self.price}'

Item = ItemDiscountReport("Брюки", 1000)
